def sandwich_toppings(*toppings):
    for i in toppings:
        print(i)
print("put toppings on sandwich like this:")
sandwich_toppings('turkey')
print("put toppings on sandwich like this:")
sandwich_toppings('turkey','durian')
print("put toppings on sandwich like this:")
sandwich_toppings('turkey','durian','pineapple')